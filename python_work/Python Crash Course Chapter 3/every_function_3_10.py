cities = ['Monument', 'Colorado Springs', 'Denver', 'Boulder', 'Fort Collins', 'Firestone', 'Thornton', 'Lafayette']
print(cities)

print("\n")
no_of_cities = len(cities)
print(no_of_cities)

print("\n")
cities.reverse()
print(cities)

print("\n")
print(sorted(cities, reverse=True))

print("\n")
print(sorted(cities))

print("\n")
cities.sort(reverse=True)
print(cities)

print("\n")
cities.reverse()
print(cities)

