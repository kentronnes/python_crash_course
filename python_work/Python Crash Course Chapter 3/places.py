places = ['Oslo', 'Sydney', 'Juneau', 'Prague', 'Gothenburg']

print(places)

##just creating an empty line so space out
print("\n")
print(sorted(places))

##creating another line break for spacing
print("\n")
print(places)

##line break
print("\n")
##print in reverse sorted order
print(sorted(places, reverse=True))

print("\n")
print(places)

print("\n")
places.reverse()
print(places)

print("\n")
places.reverse()
print(places)

print("\n")
places.sort()
print(places)

print("\n")
places.sort(reverse=True)
print(places)
